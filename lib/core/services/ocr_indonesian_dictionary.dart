  static bool isValidIndonesianWord(String word) {
    final String lower = word.toLowerCase().trim();
    final int len = lower.length;

    if (len <= 2) return true;
    if (_reNumericOnly.hasMatch(lower)) return true;
    if (commonValidWords.contains(lower)) return true;

    for (int i = 0; i < _validPrefixes.length; i++) {
      final String prefix = _validPrefixes[i];
      if (len > prefix.length + 2 && lower.startsWith(prefix)) {
        return true;
      }
    }

    for (int i = 0; i < _validSuffixes.length; i++) {
      final String suffix = _validSuffixes[i];
      if (len > suffix.length + 2 && lower.endsWith(suffix)) {
        return true;
      }
    }

    return false;
  }

  static double calculateTextQuality(String text) {
    if (text.trim().isEmpty) return 0.0;

    final List<String> rawTokens = text.split(_reWhitespace);
    int total = 0;
    int valid = 0;

    for (int i = 0; i < rawTokens.length; i++) {
      final String raw = rawTokens[i];
      if (raw.length > 1) {
        final String cleaned = raw.replaceAll(_reNonWord, '');
        if (cleaned.isNotEmpty) {
          total++;
          if (isValidIndonesianWord(cleaned)) {
            valid++;
          }
        }
      }
    }

    if (total == 0) return 0.0;
    return valid / total;
  }
